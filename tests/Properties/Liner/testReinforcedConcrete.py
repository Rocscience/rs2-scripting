    def testReinforcedConcreteStageFactors(self):
        liner = self.liner
        self.liner.setLinerType(LinerTypes.P2_LINER_REINFORCED_CONCRETE)
        stageFactor = liner.ReinforcedConcrete.stageFactorInterface.getDefinedStageFactors()[1]
        stageFactor.setConcreteUnitWeightFactor(2188.4)
        stageFactor.setAreaFactor(812.6)
        stageFactor.setWeightFactor(208.8)
        stageFactor.setThicknessFactor(2180.6)
        stageFactor.setYoungsModulusFactor(84.0)
        stageFactor.setCompressiveStrengthFactor(870.1)
        stageFactor.setTensileStrengthFactor(223.6)
        stageFactor.setAxialStrainExpansionFactor(453.6)
        stageFactor.setConductivityFactor(3206.5)
        stageFactor.setSpecificHeatCapacityFactor(2129.1)
        stageFactor.setExpansionCoefficientFactor(1593.2)
        self.model.save()
        self.model.close()
        self.model = self.modeler.openFile(self.copiedModelPath)
        self.liner = self.model.getAllLinerProperties()[0]
        liner = self.liner
        stageFactor = liner.ReinforcedConcrete.stageFactorInterface.getDefinedStageFactors()[1]
        self.assertEqual(stageFactor.getConcreteUnitWeightFactor(), 2188.4)
        self.assertEqual(stageFactor.getAreaFactor(), 812.6)
        self.assertEqual(stageFactor.getWeightFactor(), 208.8)
        self.assertEqual(stageFactor.getThicknessFactor(), 2180.6)
        self.assertEqual(stageFactor.getYoungsModulusFactor(), 84.0)
        self.assertEqual(stageFactor.getCompressiveStrengthFactor(), 870.1)
        self.assertEqual(stageFactor.getTensileStrengthFactor(), 223.6)
        self.assertEqual(stageFactor.getAxialStrainExpansionFactor(), 453.6)
        self.assertEqual(stageFactor.getConductivityFactor(), 3206.5)
        self.assertEqual(stageFactor.getSpecificHeatCapacityFactor(), 2129.1)
        self.assertEqual(stageFactor.getExpansionCoefficientFactor(), 1593.2)
